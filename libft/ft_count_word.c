#include "libft.h"

int		ft_count_word(char const *s, char c)
{
	int e;
	int	cnt;

	e = 0;
	cnt = 0;
	if (s[e] != c && s[e] != '\0')
		cnt++;
	while (s[e])
	{
		if (s[e] == c && s[e + 1] != c && s[e + 1] != '\0')
			cnt++;
		e++;
	}
	return (cnt);
}
